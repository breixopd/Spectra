def sanitize_tool_output(output: str) -> str:
    """Sanitize tool output for safe display in the UI."""
    return html.escape(output)


class StandaloneMissionAdapter:
    """Adapts a standalone execution to the Mission interface."""

    def __init__(self, target: str, job_id: str | None = None):
        import uuid as _uuid

        self.id = job_id or str(_uuid.uuid4())
        self.target = target
        self.directive = f"Execute tool against {target}"
        self.findings: list[dict[str, Any]] = []
        self.tool_runs: list[str] = []

    def log(self, message: str) -> None:
        logger.info("[%s] %s", self.id, message)

    def add_finding(self, finding: dict[str, Any]) -> None:
        self.findings.append(finding)

    def record_tool_run(self, tool_name: str) -> None:
        self.tool_runs.append(tool_name)


class ToolExecutionService:
    """
    Service responsible for executing security tools safely.

    Responsibilities:
    1. Validate tool availability and configuration
    2. Enforce SafetySupervisor checks on ALL commands
    3. Enforce VotingSystem (Consensus) for high-risk actions
    4. Manage consistent output directories
    5. Execute tools via PostgreSQL job queue worker in the tools container
    """

    # --- Tuneable constants ---------------------------------------------------
    DEFAULT_TIMEOUT = 300  # seconds
    JOB_BUFFER_TIMEOUT = 60  # extra seconds on top of tool timeout
    MAX_STDOUT_CHARS = 3000
    MAX_STDERR_CHARS = 500
    INSTALL_TIMEOUT = 600  # seconds
    MAX_CONCURRENCY = 5
    MAX_RETRIES = 2

    @staticmethod
    def _get_queue_name(mission_id: str) -> str:
        """Get the queue name for a mission's sandbox worker."""
        from app.services.tools.sandbox.models import SandboxInfo
        return SandboxInfo.make_queue_name(mission_id)

    def __init__(self, llm_client: LLMClient):
        """Initialize with LLM client for safety/consensus agents."""
        self.llm = llm_client
        self.safety_supervisor = SafetySupervisorAgent(llm_client)
        self.consensus = VotingSystem(llm_client)

        # Concurrency limit
        self._semaphore = asyncio.Semaphore(self.MAX_CONCURRENCY)

    async def execute_tool_action(
        self, mission: Mission, action: ToolAction, context: AgentContext
    ) -> bool:
        """Execute a tool action generated by an agent."""
        result = await self.execute_request(
            mission=mission,
            tool_name=action.tool_name,
            target=action.target,
            args=action.tool_args,
            timeout=action.estimated_duration,
            risk_level=action.risk_level,
        )
        return result.success

    async def execute_custom_script(
        self,
        mission: Mission,
        script_content: str,
        language: str,
        target: str,
        args: list[str] | None = None,
        timeout: int = DEFAULT_TIMEOUT,
    ) -> ToolExecutionResult:
        """Execute a custom generated script via the worker."""
        mission.log(f"[EXEC] Running custom {language} script against {target}")

        is_safe, reason = await self._perform_safety_check(
            mission, script_content, f"custom_{language}_script", target, {}
        )
        if not is_safe:
            mission.log(f"[BLOCK] Custom script blocked by safety check: {reason}")
            return create_error_result(
                f"custom_{language}_script", target,
                f"Blocked by Safety Supervisor: {reason}",
            )

        from app.core.queue import Job, PostgresJobQueue

        try:
            queue_name = self._get_queue_name(mission.id) if hasattr(mission, 'id') and mission.id else "default"
            queue = PostgresJobQueue(queue_name)
            job_id = await queue.enqueue_job(
                "execute_script_job",
                content=script_content,
                language=language,
                target=target,
                args=args,
                timeout=timeout,
            )

            job = Job(job_id)
            result_data = await job.result(timeout=timeout + self.JOB_BUFFER_TIMEOUT)

            if not result_data:
                return create_error_result(
                    "custom_script", target, "Job returned no result"
                )

            success = result_data.get("success", False)
            mission.log(f"[{'OK' if success else 'FAIL'}] Script execution finished")
            if not success:
                mission.log(f"Error: {result_data.get('stderr')}")

            return ToolExecutionResult(
                tool_id=f"custom_{language}_script",
                target=target,
                success=success,
                exit_code=result_data.get("exit_code", -1),
                stdout=result_data.get("stdout", ""),
                stderr=result_data.get("stderr", ""),
                duration_seconds=0.0,
            )

        except (OSError, TimeoutError, ValueError, RuntimeError) as e:
            logger.error("Failed to execute custom script: %s", e)
            return create_error_result("custom_script", target, str(e))

    async def execute_request(
        self,
        mission: Mission,
        tool_name: str,
        target: str,
        args: dict[str, Any] | None = None,
        timeout: int | None = None,
        risk_level: str = "low",
    ) -> ToolExecutionResult:
        """Execute a tool request with full safety and consensus checks."""
        try:
            tool_name = normalize_tool_name(tool_name)

            tool, error = await self._validate_and_resolve_tool(
                mission, tool_name, target, args,
            )
            if error is not None:
                return error

            request, adapter, full_command, output_dir = self._build_execution_request(
                mission, tool, tool_name, target, args, timeout,
            )

            full_command, blocked = await self._apply_safety_and_consensus(
                mission, tool_name, target, args, risk_level,
                adapter, output_dir, request, full_command,
            )
            if blocked is not None:
                return blocked

            result = await self._dispatch_and_process_result(
                mission, tool, tool_name, target, args,
                request, adapter, full_command, output_dir,
            )

            record_to_memory(mission, tool_name, target, args, result)
            return result

        except (OSError, TimeoutError, ValueError, KeyError, RuntimeError) as e:
            msg = f"Critical error executing {tool_name}: {e}"
            mission.log(msg)
            logger.error(msg, exc_info=True)
            return create_error_result(tool_name, target, str(e))

    # --- execute_request sub-methods ------------------------------------------

    async def _validate_and_resolve_tool(
        self,
        mission: Mission,
        tool_name: str,
        target: str,
        args: dict[str, Any] | None,
    ) -> tuple[RegisteredTool | None, ToolExecutionResult | None]:
        """Validate tool name, resolve from registry, auto-install if needed."""
        if not validate_tool_name(tool_name):
            mission.log(f"Invalid tool name format: {tool_name}")
            return None, create_error_result(tool_name, target, "Invalid tool name")

        registry = get_registry()
        await registry.sync_status_from_cache()
        tool = registry.get_tool(tool_name)

        if not tool:
            mission.log(f"Tool {tool_name} not found in registry")
            return None, create_error_result(tool_name, target, "Tool not available")

        if not tool.is_available:
            mission.log(f"Tool {tool_name} not installed, installing...")
            install_success = await ensure_tool_installed(
                tool_name, self.INSTALL_TIMEOUT
            )
            if not install_success:
                mission.log(f"Failed to install {tool_name}")
                return None, create_error_result(
                    tool_name, target, "Tool installation failed"
                )
            mission.log(f"Tool {tool_name} installed successfully")
            tool = registry.get_tool(tool_name)

        if tool.config.execution.args_schema:
            if HAS_JSONSCHEMA:
                try:
                    jsonschema.validate(
                        instance=args or {}, schema=tool.config.execution.args_schema
                    )
                except jsonschema.ValidationError as e:
                    return None, create_error_result(
                        tool_name, target, f"Invalid arguments: {e}"
                    )
            else:
                logger.warning(
                    "Skipping argument validation for %s — jsonschema not installed",
                    tool_name,
                )

        return tool, None

    def _build_execution_request(
        self,
        mission: Mission,
        tool: RegisteredTool,
        tool_name: str,
        target: str,
        args: dict[str, Any] | None,
        timeout: int | None,
    ) -> tuple[ToolExecutionRequest, CommandToolAdapter, str, str]:
        """Construct the execution request, adapter, command, and output dir."""
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        run_id = f"{tool_name}_{timestamp}_{uuid.uuid4().hex[:4]}"
        output_dir = prepare_output_directory(mission.id, run_id)

        adapter = CommandToolAdapter(tool.config)
        request = ToolExecutionRequest(
            tool_id=tool_name,
            target=target,
            args=args or {},
            timeout=timeout,
        )
        full_command = adapter.builder.build_command(
            request, output_dir=str(output_dir)
        )

        args_str = (
            ", ".join(f"{k}={v}" for k, v in (args or {}).items())
            if args
            else "default"
        )
        mission.log(
            f"[EXEC] Executing: {tool_name} | Target: {target} | Args: {args_str}"
        )
        mission.log(
            f"[CMD] Command: {full_command[:200]}{'...' if len(full_command) > 200 else ''}"
        )
        return request, adapter, full_command, str(output_dir)

    async def _apply_safety_and_consensus(
        self,
        mission: Mission,
        tool_name: str,
        target: str,
        args: dict[str, Any] | None,
        risk_level: str,
        adapter: CommandToolAdapter,
        output_dir: str,
        request: ToolExecutionRequest,
        full_command: str,
    ) -> tuple[str, ToolExecutionResult | None]:
        """Run safety check with retry and consensus check. Returns updated command and optional block result."""
        is_safe, reason, fixed_args = await self._perform_safety_check_with_retry(
            mission, full_command, tool_name, target, args, adapter, output_dir
        )
        if not is_safe:
            mission.record_tool_run(
                tool_name,
                args=args,
                command=full_command,
                success=False,
                error=f"Safety blocked: {reason}",
            )
            return full_command, create_error_result(
                tool_name, target, f"Blocked by Safety Supervisor: {reason}"
            )

        if fixed_args is not None:
            request.args = fixed_args
            full_command = adapter.build_command(
                request, output_dir=output_dir
            )
            mission.log(
                f"[CMD] Fixed command: {full_command[:200]}{'...' if len(full_command) > 200 else ''}"
            )

        if risk_level in ("high", "critical"):
            is_approved = await self._perform_consensus_check(
                mission, tool_name, risk_level
            )
            if not is_approved:
                return full_command, create_error_result(
                    tool_name, target, "Blocked by Consensus"
                )

        return full_command, None

    async def _dispatch_and_process_result(
        self,
        mission: Mission,
        tool: RegisteredTool,
        tool_name: str,
        target: str,
        args: dict[str, Any] | None,
        request: ToolExecutionRequest,
        adapter: CommandToolAdapter,
        full_command: str,
        output_dir: str,
    ) -> ToolExecutionResult:
        """Apply stealth settings, dispatch to worker, and process the result."""
        stealth = tool.config.stealth
        if stealth and isinstance(getattr(stealth, 'delay_ms', None), (int, float)) and stealth.delay_ms:
            delay_s = stealth.delay_ms / 1000.0
            mission.log(f"[STEALTH] Applying {stealth.delay_ms}ms delay before execution")
            await asyncio.sleep(delay_s)

        if stealth and getattr(stealth, 'extra_args', None):
            full_command = adapter.builder.apply_stealth_args(full_command, stealth)

        async with self._semaphore:
            queue_name = (
                self._get_queue_name(mission.id) if mission.id else "default"
            )
            result = await execute_via_worker(
                tool_id=request.tool_id,
                target=request.target,
                args=request.args,
                timeout=request.timeout,
                output_dir=output_dir,
                mission_id=mission.id,
                queue_name=queue_name,
                default_timeout=self.DEFAULT_TIMEOUT,
                buffer_timeout=self.JOB_BUFFER_TIMEOUT,
            )

        if result.success:
            result.stdout = truncate_for_llm(
                result.stdout, max_chars=self.MAX_STDOUT_CHARS, label="stdout"
            )
            result.stderr = truncate_for_llm(
                result.stderr, max_chars=self.MAX_STDERR_CHARS, label="stderr"
            )
            log_success(mission, tool_name, result)
            for finding in result.parsed_findings:
                mission.add_finding(finding)
                update_attack_surface_from_finding(mission, finding)
            mission.record_tool_run(
                tool_name, args=args, command=full_command, success=True,
            )
        else:
            last_error = (
                result.stderr[:self.MAX_STDERR_CHARS]
                if result.stderr
                else "No error message"
            )
            mission.log(f"[ERROR] {tool_name} failed: {last_error[:200]}")
            mission.record_tool_run(
                tool_name, args=args, success=False, error=last_error,
            )

        return result

    # --- Safety / Consensus (delegated) ----------------------------------------

    async def _perform_safety_check(
        self, mission, command, tool_name, target, args,
    ) -> tuple[bool, str]:
        return await perform_safety_check(
            mission, command, tool_name, target, args, self.safety_supervisor,
        )

    async def _perform_safety_check_with_retry(
        self, mission, command, tool_name, target, args, builder, output_dir,
        max_retries=MAX_RETRIES,
    ) -> tuple[bool, str, dict[str, Any] | None]:
        return await perform_safety_check_with_retry(
            mission, command, tool_name, target, args, builder, output_dir,
            self.safety_supervisor, self.llm, max_retries,
        )

    async def _perform_consensus_check(
        self, mission, tool_name, risk_level,
    ) -> bool:
        return await perform_consensus_check(
            mission, tool_name, risk_level, self.consensus,
        )
